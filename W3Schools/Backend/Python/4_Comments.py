# Use a hash sign to make a comment in Python
# This is a comment

# Use comments to allow Python to ignore the rest of the line
print("Hello World") # This is a comment

#You can use comments to prevent Python executing code
# print("Marilyn is coding")
print("Hello Marilyn")

# Use a hash sign on multiple lines ti create multiline comments
# This is a comment
# written in
# more than one line
print("Hello Python")

# You can do a multiline comment as a multiline string too
"""
This is a comment
written in
more than one line
"""
print("Hello Friends")