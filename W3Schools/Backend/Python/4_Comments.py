# Section 4: Comments


# Use a hash sign to make a comment in Python:
# This is a comment

# Use comments to allow Python to ignore the rest of the line:
print("Hello World") # this is a comment

# You can use comments to prevent Python executing code:
# print("Marilyn is coding")

# Use a hash sign on multiple lines to create multiline comments:
# this is a comment
# written in
# more than one line

# You can do a multiline comment as a multiline string too, especially as docstrings:
"""
This is a comment
written in
more than one line
"""