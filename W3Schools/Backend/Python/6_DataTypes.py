# Built-In Data Types:
# Text Type         -> str
# Numeric Types     -> int, float, complex
# Sequence Types    -> list, tuple, range
# Mapping Type      -> dict
# Set Types         -> set, frozenset
# Boolean Type      -> bool
# Binary Types      -> bytes, bytearray, memoryview
# None Type         -> NoneType

# You can get the data type of an object by using the type() function:
x = 5
print(type(x)) # integer
